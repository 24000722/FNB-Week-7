# Operators

# Addition (+)
# Subtraction (-)
# Multiplication (*)
# Division (/)
# Modulus (%)
# Exponent (**)

x = 10
y = 7

print(x + y)   # prints 17
print(x - y)   # prints 3
print(x * y)   # prints 70
print(x / y)   # prints 1.43
print(x % 7)   # prints 3

print(x**y)    # prints 10000000

